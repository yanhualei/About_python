# python的5种常见的数据类型:1.数值2.字符串3.列表4.元组5.字典
# 可变数据类型:列表和字典
# 不可变数据类型:数值,字符串,元组

L1 = [1,2,3]
print(L1,id(L1))
L1.append(4)
print(L1,id(L1))

# 字典证明是可变数据类型同上;也就是说,列表和字典的数据操作都是在同一个内存单元中进行

a = 1
print(id(a))
b = 1
print(id(b))
a = a+b
print(id(a))
# 更改数值型和字符串会将改变内存地址,

x = (1,2,[3,4])
print(id(x))


#  元组内部元素如果是可变数据类型,则内部可变,但不影响外部数据类型
x[2][0] = 5
print(x,id(x))

